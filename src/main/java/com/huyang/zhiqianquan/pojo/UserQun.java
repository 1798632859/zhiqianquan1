package com.huyang.zhiqianquan.pojo;


public class UserQun {

  private String userQunId;
  private String userId;
  private String qunId;
  private String userQunStatus;


  public String getUserQunId() {
    return userQunId;
  }

  public void setUserQunId(String userQunId) {
    this.userQunId = userQunId;
  }


  public String getUserId() {
    return userId;
  }

  public void setUserId(String userId) {
    this.userId = userId;
  }


  public String getQunId() {
    return qunId;
  }

  public void setQunId(String qunId) {
    this.qunId = qunId;
  }


  public String getUserQunStatus() {
    return userQunStatus;
  }

  public void setUserQunStatus(String userQunStatus) {
    this.userQunStatus = userQunStatus;
  }

}
